from numpy import sum, linalg, absolute

def hamming_dist(a, b, norm_weights):
    return sum(absolute(a-b)*norm_weights)

def euclidean_dist_power2(a, b, norm_weights):
    return sum(((a-b)*norm_weights)**2)

def euclidean_dist(a, b, norm_weights):
    return linalg.norm((a-b)*norm_weights)
